#Ler dois valores numéricos e exibir a diferença do maior para o menor.
a=int(input("Valor numérico inteiro um: "))
b=int(input("Valor numérico inteiro dois: "))
if a>b:
    print(f"diferença: {a-b}")
elif a==b:
    printf(f"Numeros iguais, portanto diferença é: {a-b}")
else:
    printf(f"diferença: {b-a}")